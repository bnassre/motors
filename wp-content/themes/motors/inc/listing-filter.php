			} else {
				if ( $title_generated_counter == 1 ) {
					$title_response .= ' ' . strtolower( $title_default );
				} else {
					$title_response .= $title_generated_postfix;
				}
			}

			$main_response['title'] = $title_response;
			$main_response['total'] = count( $posts );
		}

		return $main_response;
